class Item
{
  categoria;
  modelo;
  color;
  talle;
  precioUnitario;
  cantidad;
    constructor(categoria,modelo,color,talle,precioUnitario,cantidad)
    {
        this.categoria = categoria;
        this.modelo = modelo;
        this.color = color;
        this.talle = talle;
        this.precioUnitario = precioUnitario;
        this.cantidad = cantidad;
    }
}